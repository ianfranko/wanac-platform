/**
 * Get a prompt by number (1-365)
 */
export const getPromptByNumber = (number: number) => {
  return journalPrompts.find((p) => p.number === number) || null;
};

/**
 * Get the next prompt number, cycling from 1-365
 */
export const getNextPromptNumber = (currentNumber: number) => {
  if (currentNumber >= 365) return 1;
  return currentNumber + 1;
};

/**
 * Get a random prompt
 */
export const getRandomPrompt = () => {
  const randomNum = Math.floor(Math.random() * journalPrompts.length) + 1;
  return getPromptByNumber(randomNum);
};

